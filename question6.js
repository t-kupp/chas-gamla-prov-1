const strings = ["hello", "hallo", "bonjour", "hej"];

function filterStrings(arr) {
  let newArray = [];
  arr.forEach((string) => {
    if (string.length >= 4) newArray.push(string);
  });
  return newArray;
}

console.log(filterStrings(strings));
